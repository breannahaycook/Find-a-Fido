            <h2>Standard Schnauzer</h2>
            <img src="./assets/Standard Schnauzer.jpg" alt="Picture of a Standard Schnauzer">
            <p>The bold, bewhiskered Standard Schnauzer is a high-spirited farm dog from Germany. 
                They are the sometimes-willful but ever reliable medium-sized members of the Schnauzer family of 
                breeds. The Standard's sporty look is a canine classic. A medium-size dog weighing between 35 and 
                45 pounds, the Standard Schnauzer is truly the standard Schnauzer: larger than Miniatures, smaller 
                than Giants. Schnauzers of all three sizes share several breed hallmarks: a wiry, tight-fitting 
                coat of pure black or 'pepper and salt'; a robust, square-built frame; and an elongated head 
                furnished with arched eyebrows and bristly whiskers, framing eyes gleaming with keen intelligence. 
                Standards are sociable companions, alert watchdogs, enthusiastic backyard squirrel chasers, and 
                are good with kids and protective of loved ones. Approached with a firm but gentle hand, Standards 
                train beautifully. Owners must provide outlets for their dog's upbeat athleticism and highly 
                developed senses.</p>
        </section>

