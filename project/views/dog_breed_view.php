            <h2>Yorkshire Terrier</h2>
            <img src="./assets/Yorkshire-Terrier.jpg" alt="Picture of a Yorkshire Terrier">
            <p>Beneath the dainty, glossy, floor-length coat of a 
                Yorkshire Terrier beats the heart of a feisty, old-time terrier. Yorkies earned their living as 
                ratters in mines and mills long before they became the beribboned lapdogs of Victorian ladies. 
                The Yorkshire Terrier is a compact, toy-size terrier of no more than seven pounds whose crowning 
                glory is a floor-length, silky coat of steel blue and a rich golden tan. Don't let the Yorkie's 
                daintiness fool you. Tenacious, feisty, brave, and sometimes bossy, the Yorkie exhibits all the 
                traits of a true terrier. Often named the most popular dog breed in various American cities, 
                Yorkies pack lots of big-town attitude into a small but self-important package. They are favorites 
                of urbanites the world over. Yorkies are long-lived and hypoallergenic (the coat is more like 
                human hair than animal fur), and they make fine little watchdogs. This is a true 'personality 
                breed,' providing years of laughs, love, and close companionship.</p>
        </section>

