        <section>
            <h2>Border Collie</h2>
            <img src="./assets/Border-Collie.jpg" alt="Picture of a Border Collie">
            <p>A remarkably bright workaholic, the Border Collie is an amazing dog'¿maybe a bit too amazing for 
                owners without the time, energy, or means to keep it occupied. These energetic dogs will settle 
                down for cuddle time when the workday is done. Borders are athletic, medium-sized herders standing 
                18 to 22 inches at the shoulder. The overall look is that of a muscular but nimble worker 
                unspoiled by passing fads. Both the rough coat and the smooth coat come in a variety of colors 
                and patterns. The almond eyes are the focus of an intelligent expression'¿an intense gaze, the 
                Border's famous 'herding eye', is a breed hallmark. On the move, Borders are among the canine 
                kingdom's most agile, balanced, and durable citizens. The intelligence, athleticism, and 
                trainability of Borders have a perfect outlet in agility training. Having a job to perform, 
                like agility'¿or herding or obedience work'¿is key to Border happiness. Amiable among friends, 
                they may be reserved with strangers.</p>
        </section>

