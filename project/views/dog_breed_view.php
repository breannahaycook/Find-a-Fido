            <h2>Bolognese</h2>
            <img src="./assets/Bolognese.jpg" alt="Picture of a Bolognese">
            <p>The Bolognese, a member of the Bichon family, is an enchanting small, white companion dog with a 
                compact and sturdy, square body. The Bolognese is a rather serious dog and generally not high 
                energy but is intelligent and witty.
            <p>A small companion breed originating in Italy, the Bolognese is a calm, 
                faithful dog with a long, fluffy pure-white coat. They are very playful, 
                easy-going and love people. The Bolognese is small, stocky, and squarely built. 
                He is rather serene and inactive, and devoted to his master and his people. They can be shy 
                towards strangers, but warm up quickly if they are properly socialized. Prone to separation 
                anxiety, the Bolognese does not do well alone for long periods of time. They would be ill-suited 
                for people with a 9-5 workday.</p>
