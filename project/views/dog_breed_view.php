        <section>
            <h2>Basset Hound</h2>
            <img src="./assets/Basset Hound.jpg" alt="Picture of a Basset Hound">
            <p>Among the most appealing of the AKC breeds, the endearing and instantly recognizable Basset Hound 
                is a perennial favorite of dog lovers all over the world. This low-slung and low-key hound can be 
                sometimes stubborn, but is always charming. The Basset Hound stands no higher than 14 inches at 
                the shoulder but, with his remarkably heavy bone, powerful little legs, and massive paws, he 
                possesses big-dog strength and stamina. Bassets are famous for a large, domed head that features 
                extremely long, velvety ears, mournful eyes, and a wrinkled brow, which give the breed the look of 
                a sad clown. Built more for endurance than speed, the Basset moves in a deliberate but effortless 
                manner. The breed's scenting ability is uncanny; it's said that among dogs only the Bloodhound's 
                nose is more accurate. Mild and agreeable at home, the Basset is stubborn on the trail and barks 
                in a loud, ringing voice. Although they may not be wildly demonstrative in their affections, they 
                are steadfastly loyal.</p>
        </section>

