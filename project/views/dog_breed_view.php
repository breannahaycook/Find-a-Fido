        <section>
            <h2>Irish Water Spaniel</h2>
            <img src="./assets/Irish Water Spaniel.jpg" alt="Picture of a Irish Water Spaniel">
            <p>The tallest of the AKC's spaniels, the Irish Water Spaniel is instantly recognizable by its 
                crisply curled coat and tapering 'rat tail.' Among the champion swimmers of dogdom, the alert 
                and inquisitive IWS is hardworking and brave in the field, and playfully affectionate at home. 
                This tallest of AKC spaniels, standing 21 to 24 inches at the shoulder and weighing 55 to 65 
                pounds, straddles the line between 'medium' and 'large' dogs on our scale of size. Among its 
                distinguishing characteristics are a crisply curled, liver-colored, waterproof coat; a tapered 
                'rat tail'; and a cleanly chiseled head crowned with a topknot of long, loose curls. The IWS 
                moves with a smooth ground-covering gait, enabling him to put in a long day's work in the field.</p>
        </section>

